#!/usr/bin/env node
/**
 * Extracts only COPY blocks for selected public tables from a Supabase pg_dump
 * and writes them to database/seed-data.sql for use with the seed-from-dump flow.
 *
 * Tables included: companies, api_configurations, agent_categories/configurations/permissions, workflows, workflow_steps,
 * workflow_statuses, workflow_permissions, workflow_connections, workflow_categories.
 *
 * Usage: node scripts/extract-seed-data.js [dump-path]
 *   dump-path defaults to temp/database-dump.sql (relative to repo root).
 */

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const DEFAULT_DUMP = path.join(REPO_ROOT, 'temp', 'database-dump.sql');
const OUTPUT_PATH = path.join(REPO_ROOT, 'database', 'seed-data.sql');

// Only these public tables are seeded (in dependency order for COPY)
const SEED_TABLES = new Set([
  'companies',
  'api_configurations',
  'agent_categories',
  'agent_configurations',
  'agent_permissions',
  'workflows',
  'workflow_steps',
  'workflow_statuses',
  'workflow_permissions',
  'workflow_connections',
  'workflow_categories',
]);

const dumpPath = process.argv[2] ? path.resolve(process.cwd(), process.argv[2]) : DEFAULT_DUMP;

if (!fs.existsSync(dumpPath)) {
  console.error('Dump file not found:', dumpPath);
  process.exit(1);
}

const lines = fs.readFileSync(dumpPath, 'utf8').split(/\r?\n/);
const out = [];
let inCopyBlock = false;

const COPY_PUBLIC_TABLE = /^COPY public\.(\w+) /;

for (let i = 0; i < lines.length; i++) {
  const line = lines[i];

  if (inCopyBlock) {
    out.push(line);
    if (line === '\\.') {
      inCopyBlock = false;
    }
    continue;
  }

  const match = line.match(COPY_PUBLIC_TABLE);
  if (match && SEED_TABLES.has(match[1])) {
    inCopyBlock = true;
    out.push(line);
  }
}

const outputDir = path.dirname(OUTPUT_PATH);
if (!fs.existsSync(outputDir)) {
  fs.mkdirSync(outputDir, { recursive: true });
}

const header = [
  '-- Seed data extracted from Supabase dump (selected public tables only)',
  '-- Generated by scripts/extract-seed-data.js',
  '-- Disable triggers for faster load (optional).',
  'SET session_replication_role = replica;',
  '',
].join('\n');

const footer = [
  '',
  'SET session_replication_role = DEFAULT;',
  '',
].join('\n');

fs.writeFileSync(OUTPUT_PATH, header + out.join('\n') + footer, 'utf8');
console.log('Wrote', out.filter((l) => l.startsWith('COPY')).length, 'COPY blocks to', OUTPUT_PATH);
